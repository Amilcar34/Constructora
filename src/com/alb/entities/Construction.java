package com.alb.entities;

public class Construction {
	
	private String name;
	private int dni;
	
	public Construction(String name, int dni) {
		super();
		this.name = name;
		this.dni = dni;
	}

	public String getName() {
		return name;
	}

	public int getDni() {
		return dni;
	}
	
	

}
